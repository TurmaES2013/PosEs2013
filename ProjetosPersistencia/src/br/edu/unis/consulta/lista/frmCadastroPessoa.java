    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed

        SessionFactory sfac = new Configuration().configure().buildSessionFactory();
        Session se = sfac.openSession();
        Transaction tx = se.beginTransaction();

        Pessoa p = new Pessoa();
        p.setCpf(this.txtCpf.getText());
        p.setNome(this.txtNome.getText());
        p.setRg(this.txtRg.getText());
        p.setEndereco(this.txtEndereco.getText());
        p.setCidade(this.txtCidade.getText());

        se.save(p);

        tx.commit();

        JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");

    }//GEN-LAST:event_btnGravarActionPerformed

